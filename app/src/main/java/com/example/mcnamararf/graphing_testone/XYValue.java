package com.example.mcnamararf.graphing_testone;

public class XYValue {
    private int x;
    private int y;

    public XYValue (int x, int y){
        this.x = x;
        this.y = y;
    }

    public int getX(){
        return x;
    }

    public void setX(){
        this.x = x;
    }


    public int getY(){
        return y;
    }

    public void setY(){
        this.y = y;
    }

}
