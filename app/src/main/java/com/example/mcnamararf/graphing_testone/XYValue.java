package com.example.mcnamararf.graphing_testone;

public class XYValue {
    private double x;
    private double y;

    public XYValue (double x, double y){
        this.x = x;
        this.y = y;
    }

    public double getX(){
        return x;
    }

    public void setX(){
        this.x = x;
    }


    public double getY(){
        return y;
    }

    public void setY(){
        this.y = y;
    }

}
